    <section class="project-description">
        <div class="description-column-left">
            <div class="description-box">
                <h2>O que é extensão curricular na universidade?</h2>
                <p>A extensão da educação superior no Brasil é a atividade que se integra à matriz curricular e à organização da pesquisa. Ainda conforme o conselho citado, a extensão constitui-se em processo interdisciplinar, político educacional, cultural, científico e tecnológico que promove a interação transformadora entre as instituições de ensino superior e os outros setores da sociedade, por meio da produção e da aplicação do conhecimento, em articulação permanente com o ensino e a pesquisa.</p>
            </div>
            <div class="description-box">
                <h2>O que é o DiceForge?</h2>
                <p>DiceForge é um aplicativo para construção de fichas de RPG de mesa, de um modo para ser utilizado no jogo D&D feito pela empresa Wizards of the Coast. O aplicativo será um lugar para se guardar as fichas de forma mais organizada e digital facilitando a verificação de recursos que um personagem pode ter.</p>
            </div>
            <div class="description-box">
                <h2>Quando começou?</h2>
                <p>A ideia sobre o DiceForge se deu início no ano de 2023 durante um projeto que um dos professores havia passado para realizarmos. Porém, naquele momento, só tínhamos feito algumas funções de botões e o visual do aplicativo. No entanto, no ano posterior, com a matéria de extensão curricular, tivemos a possibilidade de retomarmos ele. E assim, tornamos a desenvolvê-lo.</p>
            </div>
        </div>
        <div class="description-column-right">
            <div class="description-box">
                <h2>De onde vem e para que?</h2>
                <p>O projeto DiceForge vem da necessidade de completar as Práticas de Extensão dos cursos de Ciências da Computação e Engenharia de Software na Universidade Filadélfia (UniFil), este blog/portfólio é um meio do qual iremos postar o progresso e andamento do projeto bem como as etapas que foram utilizadas para realizá-lo.</p>
            </div>
            <div class="description-box">
                <h2>Mais informações</h2>
                <p>Este é um projeto piloto que pode se expandir para outras universidades e aplicações. A ideia é fornecer uma ferramenta útil e prática para jogadores de RPG, tornando mais fácil a administração de suas fichas de personagem.</p>
            </div>
            <div class="description-box">
                <h2>Por que um aplicativo de RPG?</h2>
                <p>Devido a alguns membros do grupo pertencerem à comunidade de RPG, a ideia por trás do aplicativo era de facilitar a vida da comunidade de jogadores. Pois é difícil encontrar um aplicativo que faça essa organização, principalmente com relação às fichas dos personagens. Então a solução que o grupo encontrou foi a criação de um aplicativo que pudesse não somente criar fichas, mas também importar fichas externas e fornecer outras funções como: rolar dados, criação de habilidades, magias e etc. Tudo para facilitar a comunidade de jogadores.</p>

            </div>
        </div>
